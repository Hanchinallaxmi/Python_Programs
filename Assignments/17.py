val1=int(input("enter the value  1:"))
val2=int(input("enter the value  2:"))
opr=input("enter the operator ..(+,-,*,/)")
if opr =="+":
    print(val1+val2)
elif opr =="-":
    print(val1-val2)
elif opr =="*":
    print(val1*val2)
elif opr =="/":
    print(val1/val2)
else:
    print("wrong input,program terminated")

